// Helper function to get translation with fallback to English
export const getTranslation = (language: Language, key: string): string => {
  const keys = key.split('.');
  let value: any = translations[language];
  
  for (const k of keys) {
    value = value?.[k];
  }
  
  // Fallback to English if translation not found
  if (!value) {
    let fallback: any = translations.en;
    for (const k of keys) {
      fallback = fallback?.[k];
    }
    value = fallback;
  }
  
  return value || key;
};

export const getLanguageCode = (language: Language): string => {
  const languageCodes = {
    en: 'en-US',
    hi: 'hi-IN',
    ta: 'ta-IN',
    te: 'te-IN',
    kn: 'kn-IN',
    bn: 'bn-IN'
  };
  
  return languageCodes[language];
};

// Helper function for components to use translations
export const t = (language: Language, key: string): string => {
  return getTranslation(language, key);
};